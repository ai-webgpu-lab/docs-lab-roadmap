#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { readCsv } from "./lib/csv.mjs";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, "..");

function parseArgs(argv) {
  const options = {
    inventory: path.join(REPO_ROOT, "docs/repo-inventory.csv"),
    issues: path.join(REPO_ROOT, "issues/initial-draft-issues-30.csv"),
    output: path.join(REPO_ROOT, "tmp/projects-config.json"),
    stdout: false,
    apply: null
  };

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (token === "--inventory") {
      options.inventory = argv[index + 1];
      index += 1;
    } else if (token === "--issues") {
      options.issues = argv[index + 1];
      index += 1;
    } else if (token === "--output") {
      options.output = argv[index + 1];
      index += 1;
    } else if (token === "--stdout") {
      options.stdout = true;
    } else if (token === "--apply") {
      options.apply = argv[index + 1];
      index += 1;
    } else if (token === "--help" || token === "-h") {
      console.log("Usage: node scripts/render-projects-config.mjs [--inventory <csv>] [--issues <csv>] [--output <json>] [--stdout] [--apply <script.sh>]");
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${token}`);
    }
  }

  return options;
}

function quote(value) {
  return `'${String(value).replace(/'/g, "'\\''")}'`;
}

function buildApplyScript(config) {
  const repos = [...new Set(config.items.map((item) => item.repo))].sort();
  const lines = [
    "#!/usr/bin/env bash",
    "",
    "# Generated by scripts/render-projects-config.mjs --apply.",
    "# Review every command before running. This script does NOT execute itself.",
    "",
    "set -euo pipefail",
    "",
    `: "${"$"}{ORG:=${config.org}}"`,
    `: "${"$"}{PROJECT_TITLE:=${config.project}}"`,
    `: "${"$"}{DRY_RUN:=0}"`,
    `: "${"$"}{SKIP_PREFLIGHT:=0}"`,
    `: "${"$"}{REUSE_PROJECT:=0}"`,
    "",
    "print_command() {",
    "  printf '+'",
    `  printf ' %q' "${"$"}@"`,
    "  printf '\\n'",
    "}",
    "",
    "run() {",
    `  print_command "${"$"}@"`,
    "  if [[ \"${DRY_RUN}\" != \"1\" ]]; then",
    `    "${"$"}@"`,
    "  fi",
    "}",
    "",
    "require_command() {",
    "  if ! command -v \"$1\" >/dev/null 2>&1; then",
    "    echo \"missing required command: $1\" >&2",
    "    exit 1",
    "  fi",
    "}",
    "",
    "preflight() {",
    "  if [[ \"${DRY_RUN}\" == \"1\" ]]; then",
    "    echo \"dry-run: skipping gh/jq/auth preflight\"",
    "    return 0",
    "  fi",
    "  if [[ \"${SKIP_PREFLIGHT}\" == \"1\" ]]; then",
    "    echo \"preflight skipped by SKIP_PREFLIGHT=1\"",
    "    return 0",
    "  fi",
    "  require_command gh",
    "  require_command jq",
    "  gh auth status >/dev/null",
    "  gh api \"orgs/${ORG}\" >/dev/null",
    "  local existing_project_number",
    "  existing_project_number=\"$(gh project list --owner \"${ORG}\" --format json --jq \".projects[] | select(.title == \\\"${PROJECT_TITLE}\\\") | .number\" 2>/dev/null | head -1 || true)\"",
    "  if [[ -n \"${existing_project_number}\" && \"${REUSE_PROJECT}\" != \"1\" && -z \"${PROJECT_NUMBER:-}\" ]]; then",
    "    echo \"project already exists: ${PROJECT_TITLE}\" >&2",
    "    echo \"set REUSE_PROJECT=1 or PROJECT_NUMBER=${existing_project_number} to reuse it\" >&2",
    "    exit 1",
    "  fi",
    "  if [[ -z \"${PROJECT_NUMBER:-}\" && \"${REUSE_PROJECT}\" == \"1\" && -n \"${existing_project_number}\" ]]; then",
    "    PROJECT_NUMBER=\"${existing_project_number}\"",
    "  fi",
    "  echo \"checking repo access for " + repos.length + " repos\"",
  ];

  for (const repo of repos) {
    lines.push(`  gh repo view "${"$"}{ORG}/${repo}" >/dev/null`);
  }

  lines.push(
    "}",
    "",
    "preflight",
    "",
    "if [[ -n \"${PROJECT_NUMBER:-}\" ]]; then",
    "  echo \"Reusing project '${PROJECT_TITLE}' under org '${ORG}' (number: ${PROJECT_NUMBER})\"",
    "elif [[ \"${DRY_RUN}\" == \"1\" ]]; then",
    "  echo \"Creating project '${PROJECT_TITLE}' under org '${ORG}'\"",
    "  print_command gh project create --owner \"${ORG}\" --title \"${PROJECT_TITLE}\" --format json",
    "  PROJECT_NUMBER=\"${PROJECT_NUMBER:-0}\"",
    "else",
    "  echo \"Creating project '${PROJECT_TITLE}' under org '${ORG}'\"",
    "  PROJECT_NUMBER=\"$(gh project create --owner \"${ORG}\" --title \"${PROJECT_TITLE}\" --format json | jq -r .number)\"",
    "fi",
    "echo \"project number: ${PROJECT_NUMBER}\"",
    ""
  );

  for (const field of config.fields) {
    if (field.type === "single_select") {
      lines.push(`echo "Adding field ${field.name} (${field.type})"`);
      lines.push(`run gh project field-create "${"$"}{PROJECT_NUMBER}" --owner "${"$"}{ORG}" --name ${quote(field.name)} --data-type SINGLE_SELECT --single-select-options ${field.options.map(quote).join(",")} || true`);
    } else {
      lines.push(`echo "Adding field ${field.name} (${field.type})"`);
      lines.push(`run gh project field-create "${"$"}{PROJECT_NUMBER}" --owner "${"$"}{ORG}" --name ${quote(field.name)} --data-type TEXT || true`);
    }
  }
  lines.push("");

  const labelsByRepo = new Map();
  for (const item of config.items) {
    if (!labelsByRepo.has(item.repo)) labelsByRepo.set(item.repo, new Set());
    for (const label of item.labels) labelsByRepo.get(item.repo).add(label);
  }
  for (const [repo, labels] of [...labelsByRepo.entries()].sort(([left], [right]) => left.localeCompare(right))) {
    for (const label of [...labels].sort()) {
      lines.push(`run gh label create ${quote(label)} --color BFD4F2 --description "Auto-generated from inventory" --repo "${"$"}{ORG}/${repo}" || true`);
    }
  }
  lines.push("");

  for (const item of config.items) {
    const labels = item.labels.join(",");
    const body = `Tracked by docs/repo-inventory.csv\\nRepo: ${item.repo}\\nTrack: ${item.track}\\nPriority: ${item.priority}\\n\\n${item.summary}`;
    lines.push(`echo "Filing issue: ${item.title}"`);
    lines.push("if [[ \"${DRY_RUN}\" == \"1\" ]]; then");
    lines.push(`  print_command gh issue create --repo "${"$"}{ORG}/${item.repo}" --title ${quote(item.title)} --body ${quote(body)} --label ${quote(labels)}`);
    lines.push(`  ISSUE_URL="https://github.com/${"$"}{ORG}/${item.repo}/issues/DRY-RUN"`);
    lines.push("else");
    lines.push(`  ISSUE_URL="$(gh issue list --repo "${"$"}{ORG}/${item.repo}" --state all --search ${quote(`in:title ${item.title}`)} --json title,url --jq ${quote(`.[] | select(.title == ${JSON.stringify(item.title)}) | .url`)} | head -1 || true)"`);
    lines.push("  if [[ -n \"${ISSUE_URL}\" ]]; then");
    lines.push(`    echo "Reusing issue: ${item.title}"`);
    lines.push("  else");
    lines.push(`    ISSUE_URL="$(gh issue create --repo "${"$"}{ORG}/${item.repo}" --title ${quote(item.title)} --body ${quote(body)} --label ${quote(labels)} | tail -1)"`);
    lines.push("  fi");
    lines.push("fi");
    lines.push(`run gh project item-add "${"$"}{PROJECT_NUMBER}" --owner "${"$"}{ORG}" --url "${"$"}{ISSUE_URL}"`);
    lines.push("");
  }

  lines.push("echo \"done. items: " + config.items.length + " issues across " + new Set(config.items.map((item) => item.repo)).size + " repos\"");
  lines.push("");
  return lines.join("\n");
}

async function main() {
  const options = parseArgs(process.argv.slice(2));

  const inventoryRows = await readCsv(options.inventory);
  const issueRows = await readCsv(options.issues);

  const inventoryByRepo = new Map();
  for (const row of inventoryRows) {
    inventoryByRepo.set(row.repo, row);
  }

  const priorityCounts = { P0: 0, P1: 0, P2: 0, P3: 0 };
  for (const row of inventoryRows) {
    const priority = row.priority_group;
    if (priorityCounts[priority] !== undefined) priorityCounts[priority] += 1;
  }

  const trackCounts = new Map();
  for (const row of issueRows) {
    trackCounts.set(row.track, (trackCounts.get(row.track) || 0) + 1);
  }

  const issueRowsByRepo = new Map();
  for (const row of issueRows) {
    if (!issueRowsByRepo.has(row.repo)) issueRowsByRepo.set(row.repo, []);
    issueRowsByRepo.get(row.repo).push(row);
  }

  const items = issueRows.map((row) => {
    const inventoryRow = inventoryByRepo.get(row.repo);
    return {
      title: row.title,
      repo: row.repo,
      issue_type: row.type,
      priority: row.priority,
      track: row.track,
      summary: row.summary,
      inventory_category: inventoryRow ? inventoryRow.category : null,
      inventory_priority: inventoryRow ? inventoryRow.priority_group : null,
      labels: [
        `priority:${row.priority.toLowerCase()}`,
        `track:${row.track.toLowerCase()}`,
        `type:${row.type}`
      ],
      project_field_status: "Todo"
    };
  });

  const views = [
    {
      id: "by-priority",
      name: "By Priority",
      group_by: "priority",
      columns: ["P0", "P1", "P2", "P3"]
    },
    {
      id: "by-track",
      name: "By Track",
      group_by: "track",
      columns: [...new Set(issueRows.map((row) => row.track))].sort()
    },
    {
      id: "by-type",
      name: "By Type",
      group_by: "issue_type",
      columns: [...new Set(issueRows.map((row) => row.type))].sort()
    },
    {
      id: "by-category",
      name: "By Inventory Category",
      group_by: "inventory_category",
      columns: [...new Set(items.map((item) => item.inventory_category).filter(Boolean))].sort()
    }
  ];

  const config = {
    org: "ai-webgpu-lab",
    project: "AI WebGPU Lab — Master",
    description: "Issue-only Projects v2 board derived from docs/repo-inventory.csv and issues/initial-draft-issues-30.csv",
    fields: [
      { name: "Status", type: "single_select", options: ["Todo", "In Progress", "Done"] },
      { name: "Priority", type: "single_select", options: ["P0", "P1", "P2", "P3"] },
      { name: "Track", type: "single_select", options: views[1].columns },
      { name: "Category", type: "single_select", options: views[2].columns },
      { name: "Seed Type", type: "single_select", options: views[2].columns },
      { name: "Seed Repo", type: "text" }
    ],
    counts: {
      inventory_repos: inventoryRows.length,
      seeded_issues: issueRows.length,
      seeded_repos: issueRowsByRepo.size,
      priorities: priorityCounts,
      tracks: Object.fromEntries([...trackCounts.entries()].sort())
    },
    views,
    items
  };

  const json = JSON.stringify(config, null, 2);
  if (options.stdout) {
    process.stdout.write(`${json}\n`);
  } else {
    await fs.mkdir(path.dirname(options.output), { recursive: true });
    await fs.writeFile(options.output, `${json}\n`, "utf8");
    console.log(`projects config written to ${options.output}`);
    console.log(`- inventory repos: ${config.counts.inventory_repos}`);
    console.log(`- seeded issues: ${config.counts.seeded_issues}`);
    console.log(`- seeded repos: ${config.counts.seeded_repos}`);
    console.log(`- views: ${views.map((view) => view.id).join(", ")}`);
  }

  if (options.apply) {
    const script = buildApplyScript(config);
    await fs.mkdir(path.dirname(options.apply), { recursive: true });
    await fs.writeFile(options.apply, `${script}\n`, { encoding: "utf8", mode: 0o755 });
    console.log(`apply script written to ${options.apply} (review before running)`);
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.stack || error.message : String(error));
  process.exit(1);
});
