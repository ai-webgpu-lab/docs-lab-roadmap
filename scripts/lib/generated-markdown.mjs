import fs from "node:fs/promises";

export const GENERATED_AT_PLACEHOLDER = "__AI_WEBGPU_LAB_GENERATED_AT__";

const GENERATED_LINE_PATTERN = /^_Generated by (`[^`]+`) on .+\._$/m;
const GENERATED_AT_PATTERN = /^_Generated by `[^`]+` on (.+)\._$/m;

export async function finalizeGeneratedMarkdown({ output, text, stdout = false }) {
  const explicit = explicitGeneratedAt();
  if (explicit) {
    return text.replaceAll(GENERATED_AT_PLACEHOLDER, explicit);
  }

  const current = new Date().toISOString();
  if (stdout || !output) {
    return text.replaceAll(GENERATED_AT_PLACEHOLDER, current);
  }

  const existing = await readExisting(output);
  const existingAt = existingGeneratedAt(existing);
  const candidate = text.replaceAll(GENERATED_AT_PLACEHOLDER, current);
  if (existingAt && normalizeGeneratedLine(existing) === normalizeGeneratedLine(candidate)) {
    return text.replaceAll(GENERATED_AT_PLACEHOLDER, existingAt);
  }

  return candidate;
}

function explicitGeneratedAt() {
  if (process.env.AI_WEBGPU_LAB_GENERATED_AT) {
    return process.env.AI_WEBGPU_LAB_GENERATED_AT;
  }

  if (process.env.SOURCE_DATE_EPOCH) {
    const seconds = Number(process.env.SOURCE_DATE_EPOCH);
    if (Number.isFinite(seconds)) {
      return new Date(seconds * 1000).toISOString();
    }
  }

  return "";
}

async function readExisting(output) {
  try {
    return await fs.readFile(output, "utf8");
  } catch {
    return "";
  }
}

function existingGeneratedAt(text) {
  const match = text.match(GENERATED_AT_PATTERN);
  return match ? match[1] : "";
}

function normalizeGeneratedLine(text) {
  return text
    .replace(GENERATED_LINE_PATTERN, `_Generated by $1 on ${GENERATED_AT_PLACEHOLDER}._`)
    .replace(/\n+$/u, "\n");
}
